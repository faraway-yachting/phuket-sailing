const reverseSlugMaps: Record<string, Record<string, string>> = {}
for (const locale of Object.keys(slugTranslations)) {
  reverseSlugMaps[locale] = {}
  for (const [englishSlug, translatedSlug] of Object.entries(
    slugTranslations[locale as Locale]
  )) {
    reverseSlugMaps[locale][translatedSlug] = englishSlug
  }
}

export function translateSlug(locale: string, englishSlug: string): string {
  return slugTranslations[locale as Locale]?.[englishSlug] || englishSlug
}

export function reverseTranslateSlug(
  locale: string,
  translatedSlug: string
): string | undefined {
  return reverseSlugMaps[locale]?.[translatedSlug]
}

export function translatePath(locale: string, path: string): string {
  if (!path || path === '/') return path
  const cleanPath = path.startsWith('/') ? path.slice(1) : path
  const segments = cleanPath.split('/')
  segments[0] = translateSlug(locale, segments[0])
  return '/' + segments.join('/')
}

export function reverseTranslatePath(locale: string, path: string): string {
  if (!path || path === '/') return path
  const cleanPath = path.startsWith('/') ? path.slice(1) : path
  const segments = cleanPath.split('/')
  const englishFirst = reverseTranslateSlug(locale, segments[0])
  if (englishFirst) {
    segments[0] = englishFirst
  }
  return '/' + segments.join('/')
}
